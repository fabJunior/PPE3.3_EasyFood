package com.ppe3_3.beans;

public class Allergie {
	private int idR;
	private int idAll;
	private String libelleR;
	
	
	// GETTERS AND SETTERS
	
	public int getIdR() {
		return idR;
	}
	public void setIdR(int idR) {
		this.idR = idR;
	}
	public int getIdAll() {
		return idAll;
	}
	public void setIdAll(int idAll) {
		this.idAll = idAll;
	}
	public String getLibelleR() {
		return libelleR;
	}
	public void setLibelleR(String libelleR) {
		this.libelleR = libelleR;
	}
	
	
}
